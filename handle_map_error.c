#include "cub3d.h"

void	handle_map_error()
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (file.map[i][j])
	{
		if (file.map[i][j] == 1)
			j++;
		else
		{
			i++;
			if (file.map[i][j] == 1)
				j++;
		}
		i = 0;
	}
}
