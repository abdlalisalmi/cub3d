int wall_check(float x, float y)
{
    int map_index_X;
    int map_index_Y;

    if (x < 0 || x > WINDOW_WIDTH || y < 0 || y > WINDOW_HIGHT)
        return (1);
    map_index_X = floorf(x / SQUARE);
    map_index_Y = floorf(y / SQUARE);
    return (MAP[map_index_Y][map_index_X]);
}

void    draw_squar(t_struct *data, int x, int y, int color)
{
	int i;
	int j;
	j = y;
	while (j < y + SQUARE)
	{
		i = x;
		while (i < x + SQUARE)
		{
			data->img_matrix[((int)j * WINDOW_WIDTH + (int)i)] = color;
			i++;
		}
		j++;
	}
}

void draw_map(t_struct *data)
{
	int color;
	int	i;
	int	j;
	int	x;
	int	y;

	j = 0;
	while (j < NUM_ROWS)
	{
		i = 0;
		while (i < NUM_COLS)
		{
			x = i * SQUARE;
			y = j * SQUARE;
			color = MAP[j][i] == 1 ? 0xFFFFFF : 0x000000;
			draw_squar(data, x, y,color);
			i++;
		}
		j++;
	}
}
