void    draw_squar(t_struct *data, int x, int y)
{
    int tmp = y;
    int i = 0;
    int j = 0;
    while (i <= SQUARE)
    {
        while (j <= SQUARE)
        {
            mlx_pixel_put(data->connection_id, data->window_id, x, y, 0xFFFF00);
            j++;
            y++;
        }
        j = 0;
        i++;
        x++;
        y = tmp;
    }
}

void draw_map(t_struct *data)
{
	int i;
	int j;
	int x;
	int y;

	i = 0;
	j = 0;
	x = 0;
	y = 0;
	while (y <= WINDOW_HIGHT)
	{
		while (x <= WINDOW_WIDTH)
		{
			if (MAP[i][j] == 1)
				draw_squar(data, x, y);
			x+=SQUARE;
			j++;
		}
		j = 0;
		x = 0;
		y += SQUARE;
		i++;
	}
}
